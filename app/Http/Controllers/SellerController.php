     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        request()->validate([
            'name' => 'required',
            'cnpj' => ['required', 'unique:sellers,cnpj', new CNPJ]
        ]);

        return Seller::create([
            'name' => strtolower(request('name')),
            'cnpj' => request('cnpj')
        ]);
    }

